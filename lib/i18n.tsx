interface I18nContextValue {
  locale: Locale
  setLocale: (next: Locale) => void
  t: (key: string, vars?: Record<string, string | number>) => string
}

const I18nContext = createContext<I18nContextValue | undefined>(undefined)
const STORAGE_KEY = "preferred_language"

function isSupportedLocale(val: string | null): val is Locale {
  return !!val && (SUPPORTED_LOCALES as string[]).includes(val)
}

export function I18nProvider({ children, initialLocale = "en" }: { children: ReactNode; initialLocale?: Locale }) {
  const [locale, setLocaleState] = useState<Locale>(initialLocale)

  const setLocale = useCallback((next: Locale) => {
    setLocaleState(next)
    if (typeof window !== "undefined") {
      localStorage.setItem(STORAGE_KEY, next)
      document.cookie = `${STORAGE_KEY}=${next}; path=/; max-age=31536000`
    }
  }, [])

  const t = useCallback(
    (key: string, vars?: Record<string, string | number>) => {
      const template = translations[locale]?.[key] ?? translations.en[key] ?? key
      if (!vars) return template
      return Object.entries(vars).reduce((str, [k, v]) => str.replace(`{${k}}`, String(v)), template)
    },
    [locale]
  )

  const value = useMemo(() => ({ locale, setLocale, t }), [locale, setLocale, t])

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n() {
  const ctx = useContext(I18nContext)
  if (!ctx) {
    throw new Error("useI18n must be used within an I18nProvider")
  }
  return ctx
}

const dateLocaleMap: Record<Locale, string> = {
  en: "en-US",
  tr: "tr-TR",
  ar: "ar",
  bs: "bs-BA",
  de: "de-DE",
  fi: "fi-FI",
  da: "da-DK",
  ru: "ru-RU",
}

export function getDateLocale(locale: Locale) {
  return dateLocaleMap[locale] ?? "en-US"
}

